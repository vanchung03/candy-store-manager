    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
     // Hiển thị hộp thoại xác nhận
int confirmed = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa lịch sử giao dịch?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
int maHD = Integer.parseInt(txtMaHoaDon.getText());
// Kiểm tra người dùng đã chọn "Yes" hay "No"
if (confirmed == JOptionPane.YES_OPTION) {
    // Người dùng chọn "Yes", tiến hành xóa
    boolean result = hoaDonData.deleteHoaDon(maHD);

    if (result) {
        JOptionPane.showMessageDialog(this, "Xóa lịch sử giao dịch thành công");
        // Sau khi xóa xong, có thể làm mới bảng hoặc cập nhật bảng
        // refreshTable();
        loadDataToTable();
    } else {
        JOptionPane.showMessageDialog(this, "Xóa lịch sử thất bại");
    }
} else {
    // Người dùng chọn "No", không thực hiện xóa
    JOptionPane.showMessageDialog(this, "Đã hủy xóa lịch sử giao dịch");
}
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnTimHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimHoaDonMouseClicked
     int maHoaDon = Integer.parseInt(txtMaHoaDon.getText());
    
    // Gọi phương thức tìm kiếm hóa đơn theo mã
    Vector<HoaDon> danhSachHoaDon = hoaDonData.searchHoaDonByMaHoaDon(maHoaDon);
    
    // Kiểm tra xem có kết quả tìm kiếm hay không
    if (!danhSachHoaDon.isEmpty()) {
        // Cập nhật bảng table_HoaDon với danh sách hóa đơn tìm kiếm
       updateTableHoaDon(danhSachHoaDon);
    } else {
        // Hiển thị thông báo nếu không tìm thấy hóa đơn
        JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn có mã " + maHoaDon,
                "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnTimHoaDonMouseClicked

