    private void loadDataToTable() {
        Vector<Vector<Object>> data = hoaDonData.getHoaDonData();
        Vector<String> columnNames = hoaDonData.getColumnNames();
        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        table_HoaDon.setModel(model);
    }
    // Khai báo phương thức updateTableHoaDon
    private void updateTableHoaDon(Vector<HoaDon> danhSachHoaDon) {
    DefaultTableModel model = (DefaultTableModel) table_HoaDon.getModel();
    model.setRowCount(0); // Xóa tất cả các dòng hiện có trong bảng

    for (HoaDon hoaDon : danhSachHoaDon) {
        // Thêm từng hóa đơn vào bảng
        model.addRow(new Object[]{
            hoaDon.getMaHD(),
            hoaDon.getMaNV(),
            hoaDon.getNgayLap(),
            hoaDon.getTongTien()
        });
    }
}

