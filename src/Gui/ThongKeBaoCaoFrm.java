    private void btnTongDoanhThuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTongDoanhThuMouseClicked
   int selectedMonth = Integer.parseInt(cbTheoThang.getSelectedItem().toString());
    
    // Assuming you have a method tinhTongTienTheoThang(year, month) to calculate the total revenue
    // You can call this method to get the total revenue for the selected month
    int year = 2023; // You can replace this with the desired year
    float totalRevenue = hoaDonData.tinhTongTienTheoThang(year, selectedMonth);
    
    if (totalRevenue != 0.0f) {
        // Display a message dialog with the calculated total revenue
        
        JOptionPane.showMessageDialog(this, String.format("Tổng doanh thu tháng %d: %.3fĐ", selectedMonth, totalRevenue), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        txtTongDoanhThu.setText(String.format("%.3fĐ", totalRevenue));
    } else {
        // Display a message dialog indicating that there is no data
        JOptionPane.showMessageDialog(this, "Không có dữ liệu cho tháng này.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnTongDoanhThuMouseClicked

