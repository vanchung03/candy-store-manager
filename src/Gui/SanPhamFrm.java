    public void loadMaNNCC() {
    try {
        conn = connectDB.getConnection();
        String sql = "SELECT MaNCC FROM NhaCungCap";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        
        // Xóa tất cả các mục hiện có trong JComboBox
        cbMaNhaCungCap.removeAllItems();
        
        while (rs.next()) {
            String maNCC = rs.getString("MaNCC");
            cbMaNhaCungCap.addItem(maNCC);
        }
        
        st.close();
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
    }
    private void loadDataToTable() {
    Vector<Vector<Object>> data = sanPhamData.getData();
    Vector<String> columnNames = sanPhamData.getColumnNames();
    DefaultTableModel model = new DefaultTableModel(data, columnNames);
    table_SanPham.setModel(model);
    

    // Bắt sự kiện khi chọn một dòng trong bảng
     table_SanPham.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
        @Override
        public void valueChanged(ListSelectionEvent e){
           if (!e.getValueIsAdjusting()) {
               int selectedRow = table_SanPham.getSelectedRow();
           if(selectedRow >= 0){
               txtMaSanPham.setText(table_SanPham.getValueAt(table_SanPham.getSelectedRow(),0)+"");
               txtTenSanPham.setText(table_SanPham.getValueAt(table_SanPham.getSelectedRow(),1)+"");
               txtMoTa.setText(table_SanPham.getValueAt(table_SanPham.getSelectedRow(),2)+"");
               txtGiaTien.setText(table_SanPham.getValueAt(table_SanPham.getSelectedRow(),3)+"");
               txtSoLuong.setText(table_SanPham.getValueAt(table_SanPham.getSelectedRow(),4)+"");
               String selectedMaNhaCungCap = table_SanPham.getValueAt(table_SanPham.getSelectedRow(), 5) + "";
               cbMaNhaCungCap.setSelectedItem(selectedMaNhaCungCap);
         }
       }
       }
        
    });
    }




