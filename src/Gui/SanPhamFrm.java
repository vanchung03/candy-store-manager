    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        // TODO add your handling code here:
        int maSP = Integer.parseInt(txtMaSanPham.getText());
        String tenSP = txtTenSanPham.getText();
        String moTa = txtMoTa.getText();
        float gia = Float.parseFloat(txtGiaTien.getText());
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        int mncc = Integer.parseInt(cbMaNhaCungCap.getSelectedItem().toString());

        SanPham sp = new SanPham(maSP, tenSP, moTa, gia, soLuong, mncc);
        if (sanPhamData.updateSanPham(sp)) { // Tạo hàm updateSanPham để cập nhật sản phẩm
            JOptionPane.showMessageDialog(this, "Sửa Sản Phẩm thành công.");
            loadDataToTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa sản Phẩm thất bại.");
        }
    }//GEN-LAST:event_btnSuaMouseClicked

