    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
    int maNCC = Integer.parseInt(txtMaNCC.getText());
        String tenNCC = txtTenNCC.getText();
        String diaChi = txtDiaChi.getText();
        String dienThoai = txtDienThoai.getText();
        String email = txtEmail.getText();
        
        // Tạo đối tượng NhaCungCap
        NhaCungCap nhaCungCap = new NhaCungCap(maNCC, tenNCC, diaChi, dienThoai, email); 
        if (nhaCungCapData.insertNhaCungCap(nhaCungCap)) {
            JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thành công.");
            loadDataToTable();
            clearTextFields();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thất bại.");
        }
    }//GEN-LAST:event_btnThemMouseClicked

    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
    int maNCC = Integer.parseInt(txtMaNCC.getText());
    String tenNCC = txtTenNCC.getText();
    String diaChi = txtDiaChi.getText();
    String dienThoai = txtDienThoai.getText();
    String email = txtEmail.getText();
    NhaCungCap ncc = new NhaCungCap(maNCC, tenNCC, diaChi, dienThoai, email);
    boolean result = nhaCungCapData.updateNhaCungCap(ncc);
    if (result) {
        JOptionPane.showMessageDialog(this, "Sửa thông tin nhà cung cấp thành công");
        clearTextFields();
         loadDataToTable();
    } else {
        JOptionPane.showMessageDialog(this, "Sửa thông tin nhà cung cấp thất bại");
    }
       
    }//GEN-LAST:event_btnSuaMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // Lấy mã nhà cung cấp từ JTextField
    int maNCC = Integer.parseInt(txtMaNCC.getText());
    boolean result = nhaCungCapData.deleteNhaCungCap(maNCC);
    if (result) {
        JOptionPane.showMessageDialog(this, "Xóa nhà cung cấp thành công");
        clearTextFields();
        loadDataToTable();
    } else {
        JOptionPane.showMessageDialog(this, "Xóa nhà cung cấp thất bại");
    }
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnTimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimMouseClicked
        // TODO add your handling code here:
         String tuKhoa = txtTim.getText();

    // Gọi phương thức tìm kiếm từ lớp NhaCungCapData
    Vector<Vector<Object>> data = nhaCungCapData.timNhaCungCap(tuKhoa);

    // Kiểm tra và hiển thị kết quả tìm kiếm
    if (!data.isEmpty()) {
        // Cập nhật dữ liệu cho bảng
        DefaultTableModel model = (DefaultTableModel) table_NhaCungCap.getModel();
        model.setRowCount(0); // Xóa tất cả dòng trong bảng

        for (Vector<Object> row : data) {
            model.addRow(row); // Thêm từng dòng tìm được vào bảng
        }
    } else {
        // Hiển thị thông báo nếu không tìm thấy kết quả
        JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả nào.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnTimMouseClicked

