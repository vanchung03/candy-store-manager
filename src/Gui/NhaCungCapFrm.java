    private void loadDataToTable() {
        Vector<Vector<Object>> data = nhaCungCapData.getData();
        Vector<String> columnNames = nhaCungCapData.getColumnNames();
        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        table_NhaCungCap.setModel(model);
        
        // Bắt sự kiện khi chọn một dòng trong bảng
        table_NhaCungCap.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int selectedRow = table_NhaCungCap.getSelectedRow();
                    if (selectedRow >= 0) {
                        // Lấy dữ liệu từ dòng được chọn và hiển thị lên các JTextField
                        txtMaNCC.setText(model.getValueAt(selectedRow, 0).toString());
                        txtTenNCC.setText(model.getValueAt(selectedRow, 1).toString());
                        txtDiaChi.setText(model.getValueAt(selectedRow, 2).toString());
                        txtDienThoai.setText(model.getValueAt(selectedRow, 3).toString());
                        txtEmail.setText(model.getValueAt(selectedRow, 4).toString());
                    }
                }
            }
        });
    }
    private void clearTextFields() {
    txtMaNCC.setText("");
    txtTenNCC.setText("");
    txtDiaChi.setText("");
    txtDienThoai.setText("");
    txtEmail.setText("");
}


   


